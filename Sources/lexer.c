#include "../Includes/shell.h"

int		is_operand(char *c)
{
	if ((c[0] == '>' && c[1] == '>') || (c[0] == '<' && c[1] == '<'))
		return (1);
	else if ((c[0] == '&' && c[1] == '&') || (c[0] == '|' && c[1] == '|'))
		return (1);
	else if (ft_strchr("<>;&| ", c[0]))
		return (1);
	return (0);
}
